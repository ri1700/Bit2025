package drill02;

public class ex05 {

	public static void main(String[] args) {
		
		/**
		 * 문제 5. 산술/논리 연산
		 * 
		 * int a = 7; int b = 3; System.out.println(a + b); System.out.println(a / b);
		 * System.out.println(a % b); System.out.println(a > b && b > 0);
		 * System.out.println(!(a == 7));
		 * 
		 */
		
		int a = 7;
		int b = 3;
		
		System.out.println(a + b);	// 10
		System.out.println(a / b);	// 2
		System.out.println(a % b);	// 1
		System.out.println(a > b && b > 0);	// 참	
		System.out.println(!(a == 7));	// 거짓
		
	}

}
